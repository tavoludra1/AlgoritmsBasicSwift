import Foundation

// An Array of `String` elements O(1) constant-time operation
var people = ["Brian", "Stanley", "Ringo"]
print(people[0])
print(people[1])
print(people[2])

people.append("Charles")
print(people)

// to insert an element in a particular location O(n) linear time
people.insert("Andy", at: 2)
print(people)







